package com.vphoainha.itfmobile.model;

public class Tag {
	private int id, numRefer;
	private String name;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getNumRefer() {
		return numRefer;
	}
	public void setNumRefer(int numRefer) {
		this.numRefer = numRefer;
	}
	
	
}
